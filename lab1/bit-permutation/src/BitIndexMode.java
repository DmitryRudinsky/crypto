/**
 * Режим индексации битов внутри одного байта.
 */
public enum BitIndexMode {
    /**
     * Normal - биты индексируются от младшего (LSB) к старшему (MSB).
     * Пример: для байта 0b10101010
     * - бит 0 (LSB) = 0
     * - бит 1 = 1
     * - бит 7 (MSB) = 1
     */
    NORMAL,
    
    /**
     * Inverted - биты индексируются от старшего (MSB) к младшему (LSB).
     * Пример: для байта 0b10101010
     * - бит 0 (MSB) = 1
     * - бит 1 = 0
     * - бит 7 (LSB) = 0
     */
    INVERTED
}

